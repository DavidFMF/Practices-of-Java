package BasicosParaPrincipiantes;

public class Ejercicio3 {

	public static void main(String[] args) {
		
		//Declarar una variable con un valor. Incrementarla en 77, decrementarla en 3 y duplicar su valor.
		
		int N = 44;
		
		System.out.println("Valor inicial de N: " + N);
		N+= 77;
		System.out.println("Incrementamos N en 77: " + N);
		N-= 3;
		System.out.println("Decrementamos N en 3: " + N);
		N*= 2;
		System.out.println("Duplicamos el valor de N: " + N);
		
	}
}
