package BasicosParaPrincipiantes;

public class Ejercicio1 {
	
	public static void main(String[] args) {
		
		//Declarar 3 variables con sus valores y mostrar resultados entre ellas.
		
		int N = 5;
		double A = 3;
		char C = 'a';
		
		System.out.println("Valor de N: " + N);
		System.out.println("Valor de A: " + A);
		System.out.println("Valor de C: " + C);
		
		System.out.println("La suma de N + A da como resultado: " + (N+A));
		System.out.println("La resta de N - A da como resultado: " + (N-A));
		System.out.println("El valor numérico de C da como resultado: " + (int)C);
		
	}

}
