package BasicosEstructuraIterativaORepetitiva;

public class Ejercicio5 {

	public static void main(String[] args) {
		
		//Mostrar los números del 100 al 1 usando un bucle do...while.
		
		int i = 100;
		
		do{
			System.out.println(i);
			i--;
		}while
			(i >= 1);
			
		
	}

}
